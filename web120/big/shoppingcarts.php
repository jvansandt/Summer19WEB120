<h2>Using Shopping Carts on Your Website</h2>
    
<p>An online shopping cart can be necessary for your business, or can boost sales if you already have a physical store. According to websitebuilderexpert.com, “ecommerce sales currently account for 17.2% of all retail sales, with online shopping itself growing 13.7% year-on-year (Carmichael).”</p>
    
<p>Ecommerce platforms, or online website builders, can be used to easily set up a shop for your website without much effort; just a few hours or less. A developer can still create one using HTML, CSS, and a programming language such as PERL or JavaScript, but making use of a builder will allow the third party to deal with security certificates, hosting, marketing, among other things which streamline the process (Easy E-Commerce). The ecommerce blog, 3dcart.com, suggests an HTML shopping cart as the most versatile option, since it will be compatible with any device (Cannon). They do mention that HTML carts require cookies enabled, but also that users are accustomed to that so it won’t catch many people by surprise.</p>

<p>There are many different options to choose from when it comes to using an ecommerce platform, giving you flexibility between ease of use and expandability, with a range of price options. If you prefer to create your own shopping cart there’s a lot to consider, especially when it comes to security of information, but it’s not all from scratch because there are options to assist with the HTML and manage money such as Paypal has.</p>
